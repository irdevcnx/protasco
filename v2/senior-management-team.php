// Logic to handle multiple leaders with one modal
const leaderModal = document.getElementById('leaderModal');
leaderModal.addEventListener('show.bs.modal', function (event) {
  // Button that triggered the modal
  const button = event.relatedTarget;
  
  // Extract info from data-attributes
  const name = button.getAttribute('data-bs-name');
  const role = button.getAttribute('data-bs-role');
  const bio = button.getAttribute('data-bs-bio');
  const img = button.getAttribute('data-bs-img');

  // Update the modal's content
  leaderModal.querySelector('#modalName').textContent = name;
  leaderModal.querySelector('#modalRole').textContent = role;
  leaderModal.querySelector('#modalBio').textContent = bio;
  leaderModal.querySelector('#modalImg').src = img;
});
</script>